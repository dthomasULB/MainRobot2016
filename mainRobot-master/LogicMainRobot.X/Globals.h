#ifndef GLOBALS_H
#define	GLOBALS_H


#define PI (3.14159)

#define MAX(a, b)   (((a) < (b)) ? (b) : (a))
#define MIN(a, b)   (((a) > (b)) ? (a) : (b))
#define ABS(a)		(((a) < 0) ? -(a) : (a))
#define SIGN(a)		(((a) < 0) ? -1 : 1)
#define DEG2RAD(x) (x*(PI/1800))

#endif	/* GLOBALS_H */

